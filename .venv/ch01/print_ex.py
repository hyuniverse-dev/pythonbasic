# print() 함수
#   - console view(콘솔창)에 값을 출력해주는 함수

'''
(1) sep 옵션
    - 출력문에서 띄워쓰기 형식을 지정할 수 있다.
'''
print(1, 2, 3, 10, 20, 30, sep='/')
print(123, 456, 789, sep='-')

'''
(2) end 옵션
    - 출력문에서 문장의 마지막을 지정할 수 있다.
'''
print("Hello!", end=" ")
print("World!")
print("Hello!\nPyCharm!") # \n 은 개행처리를 한다.








